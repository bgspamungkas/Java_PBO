public class PersegiPanjang {
    // atribut
    int panjang;
    int lebar;
    
    // method
    static int perkalian(int a, int b){
        int hasil = a * b;
        return hasil;}
    
    int hitungLuas(int panjang, int lebar){
        int luas = perkalian(panjang, lebar);
        return luas;}

    

}
